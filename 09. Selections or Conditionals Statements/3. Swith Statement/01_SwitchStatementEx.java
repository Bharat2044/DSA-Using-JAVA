public class SwitchStatementEx {
    public static void main(String[] args) {
        int num = 3;
    
        switch(num) {
            case 1:
                System.out.println("Number is " + num);
                break;
                
            case 2:
                System.out.println("Number is " + num);
                break;
                
            case 3:
                System.out.println("Number is " + num);
                break;
                
            case 4:
                System.out.println("Number is " + num);
                break;
                
            default:
                System.out.println("Default Case");
        }
    }
}