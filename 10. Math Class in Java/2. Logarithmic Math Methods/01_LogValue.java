public class LogValue {
    public static void main(String[] args) {

        System.out.println(Math.log(5)); 
        System.out.println(Math.log(-5)); 
        System.out.println(Math.log(0)); 
        
        System.out.println(Math.log(68)); 
        System.out.println(Math.log(100)); 
        
        System.out.println(Math.log(38.9)); 
        System.out.println(Math.log(-70.4)); 
        
        System.out.println(Math.log(1.0/0)); 
        System.out.println(Math.log(0.0/0)); 
        
        System.out.println((int)Math.log(10)); 
        System.out.println((int)Math.log(85)); 
        System.out.println((int)Math.log(110)); 
    }
}