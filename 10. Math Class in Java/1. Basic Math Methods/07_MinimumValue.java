public class MinimumValue {
    public static void main(String[] args) {
      
        System.out.println(Math.min(20, 50));   
        System.out.println(Math.min(345L, 3458L));
        
        System.out.println(Math.min(-25.74f, -20.38f));
        System.out.println(Math.min(-25.67, 87.232));
        
        System.out.println(Math.min(1, Math.min(5, -3)));
    }
}