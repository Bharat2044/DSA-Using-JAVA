public class PowerValue {
    public static void main(String[] args) {
      
        System.out.println(Math.pow(5, 3));   
        System.out.println(Math.pow(-5, 3));   
        System.out.println(Math.pow(5, -3));   
        System.out.println(Math.pow(-5, -3));   
        
        System.out.println(Math.pow(2.5, 4));   
        System.out.println(Math.pow(9.0, -2));   
        System.out.println(Math.pow(-765, 0.2));   
    }
}