public class MaximumValue {
    public static void main(String[] args) {
      
        System.out.println(Math.max(20, 50));   
        System.out.println(Math.max(345L, 3458L));
        
        System.out.println(Math.max(-25.74f, -20.38f));
        System.out.println(Math.max(-25.67, 87.232));
        
        System.out.println(Math.max(1, Math.max(5, -3)));
    }
}