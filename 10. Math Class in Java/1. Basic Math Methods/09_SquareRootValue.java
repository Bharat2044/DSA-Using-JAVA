public class SquareRootValue {
    public static void main(String[] args) {
      
        System.out.println(Math.sqrt(5));  
        System.out.println(Math.sqrt(81));  
        System.out.println(Math.sqrt(-5));  
        
        System.out.println(Math.sqrt(0.0));  
        System.out.println(Math.sqrt(4.5));  
        System.out.println(Math.sqrt(16.9f));  
        
        System.out.println(Math.sqrt(1.0/0));  
        System.out.println(Math.sqrt(0.0/0));  
    }
}