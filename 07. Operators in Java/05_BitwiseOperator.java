public class BitwiseOperator {
    public static void main(String[] args) {
        int a = 5;
        int b = 2;
        
        System.out.println(a & b);
        System.out.println(a | b);
        System.out.println(a ^ b);
        System.out.println(a >> b);
        System.out.println(a >>> b);
        System.out.println(a << b);
        System.out.println(~a);
    }
}
