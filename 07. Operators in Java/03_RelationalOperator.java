public class RelationalOperator {
    public static void main(String[] args) {
        int a = 7, b = 11;
    
        // value of a and b
        System.out.println("a is " + a + " and b is " + b);
    
        // == operator
        System.out.println(a == b); 
    
        // != operator
        System.out.println(a != b);  
    
        // > operator
        System.out.println(a > b);
    
        // < operator
        System.out.println(a < b);  
    
        // >= operator
        System.out.println(a >= b);  
    
        // <= operator
        System.out.println(a <= b); 
    }
}
