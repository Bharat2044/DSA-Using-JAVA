public class ArrayDatatype {
        
    public static void main(String args[]) {
        int []a = {1, 2, 3};    // int a[] = {1, 2, 3};  or  // int[] a = {1, 2, 3};
        
        int []b = new int[]{4, 5, 6};
        
        System.out.println(a);
        System.out.println(a[0]);
        
        System.out.println(b);
        System.out.println(b[0]);
    }
}
